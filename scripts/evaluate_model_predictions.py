#!/usr/bin/env python3
"""Evaluate QLoRA model predictions against the held-out QA test split.

This evaluates the model output, not the synthetic dataset artifact. It expects
JSONL generated by scripts/inference_qlora.py and computes complementary scores:

- faithfulness: generated answer support in source_context
- answer_relevancy: generated answer addresses question
- answer_correctness: generated answer matches the reference answer
- semantic_similarity: embedding similarity to the reference answer

Example:
    python scripts/evaluate_model_predictions.py \
        --input outputs/gemma4-grounded/test_predictions.jsonl \
        --output outputs/gemma4-grounded/test_eval.json \
        --limit 25
"""

from __future__ import annotations

import argparse
import asyncio
import json
import statistics
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Evalua predicciones de modelos fine-tuneados sobre el split test."
    )
    parser.add_argument("--input", type=Path, required=True, help="JSONL de predicciones.")
    parser.add_argument("--output", type=Path, default=None, help="Reporte JSON de salida.")
    parser.add_argument("--limit", type=int, default=None, help="Evalua solo los primeros N ejemplos.")
    parser.add_argument("--timeout-seconds", type=int, default=180)
    parser.add_argument("--llm-model", type=str, default="gpt-4o-mini")
    parser.add_argument("--embedding-model", type=str, default="text-embedding-3-small")
    return parser.parse_args()


def read_jsonl(path: Path) -> list[dict[str, Any]]:
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def extract_metric_value(result: Any) -> float | None:
    if isinstance(result, (int, float)):
        return float(result)
    if isinstance(result, dict):
        value = result.get("value")
    else:
        value = getattr(result, "value", None)
    return None if value is None else float(value)


def extract_metric_reason(result: Any) -> str:
    if isinstance(result, dict):
        return str(result.get("reason", "") or "")
    return str(getattr(result, "reason", "") or "")


def get_field(row: dict[str, Any], canonical: str, legacy: str | None = None) -> str:
    value = row.get(canonical)
    if value is None and legacy:
        value = row.get(legacy)
    return str(value or "").strip()


def configure_gpt5_compat(llm: Any, model_name: str) -> None:
    model_lower = model_name.lower()
    if model_lower.startswith("gpt-5.") and hasattr(llm, "model_args") and isinstance(llm.model_args, dict):
        llm.model_args.pop("max_tokens", None)
        llm.model_args.setdefault("max_completion_tokens", 1024)
        llm.model_args["temperature"] = 1.0
        llm.model_args.pop("top_p", None)


async def evaluate_predictions(
    rows: list[dict[str, Any]],
    llm_model: str,
    embedding_model: str,
    timeout_seconds: int,
) -> list[dict[str, Any]]:
    try:
        from openai import AsyncOpenAI
        from ragas.embeddings.base import embedding_factory
        from ragas.llms import llm_factory
        from ragas.metrics.collections import (
            AnswerCorrectness,
            AnswerRelevancy,
            Faithfulness,
            SemanticSimilarity,
        )
    except ImportError as exc:
        raise SystemExit("Faltan dependencias. Ejecuta: pip install -r requirements.txt") from exc

    client = AsyncOpenAI()
    llm = llm_factory(llm_model, client=client)
    configure_gpt5_compat(llm, llm_model)
    embeddings = embedding_factory("openai", model=embedding_model, client=client)

    faithfulness = Faithfulness(llm=llm)
    relevancy = AnswerRelevancy(llm=llm, embeddings=embeddings)
    correctness = AnswerCorrectness(llm=llm, embeddings=embeddings)
    similarity = SemanticSimilarity(embeddings=embeddings)

    results: list[dict[str, Any]] = []
    total = len(rows)
    for idx, row in enumerate(rows, start=1):
        qa_id = row.get("id") or row.get("qa_id") or row.get("metadata", {}).get("qa_id") or idx
        question = get_field(row, "question", "pregunta")
        generated = get_field(row, "generated_answer", "generated")
        reference = get_field(row, "reference_answer", "respuesta")
        context = get_field(row, "source_context", "contexto_fuente")

        print(f"[EVAL] {idx}/{total} qa_id={qa_id}", flush=True)
        scored: dict[str, Any] = {
            "qa_id": qa_id,
            "question": question,
            "generated_answer": generated,
            "reference_answer": reference,
            "source_context": context,
            "metadata": row.get("metadata", {}),
        }
        errors: list[str] = []

        metric_calls = {
            "faithfulness": faithfulness.ascore(
                user_input=question,
                response=generated,
                retrieved_contexts=[context] if context else [],
            ),
            "answer_relevancy": relevancy.ascore(
                user_input=question,
                response=generated,
            ),
            "answer_correctness": correctness.ascore(
                user_input=question,
                response=generated,
                reference=reference,
            ),
            "semantic_similarity": similarity.ascore(
                response=generated,
                reference=reference,
            ),
        }

        for name, metric_call in metric_calls.items():
            try:
                metric_result = await asyncio.wait_for(metric_call, timeout=timeout_seconds)
                scored[name] = extract_metric_value(metric_result)
                reason = extract_metric_reason(metric_result)
                if reason:
                    scored[f"{name}_reason"] = reason
            except Exception as exc:
                scored[name] = None
                errors.append(f"{name}: {type(exc).__name__} {exc!r}")

        if errors:
            scored["error"] = " | ".join(errors)
        results.append(scored)
    return results


def mean_score(rows: list[dict[str, Any]], key: str) -> float | None:
    values = [row[key] for row in rows if row.get(key) is not None]
    return round(statistics.mean(values), 4) if values else None


def summarize(results: list[dict[str, Any]]) -> dict[str, Any]:
    return {
        "predictions_evaluated": len(results),
        "predictions_with_error": sum(1 for row in results if row.get("error")),
        "avg_faithfulness": mean_score(results, "faithfulness"),
        "avg_answer_relevancy": mean_score(results, "answer_relevancy"),
        "avg_answer_correctness": mean_score(results, "answer_correctness"),
        "avg_semantic_similarity": mean_score(results, "semantic_similarity"),
    }


async def main_async(args: argparse.Namespace) -> None:
    rows = read_jsonl(args.input)
    if args.limit is not None:
        rows = rows[: args.limit]
    results = await evaluate_predictions(rows, args.llm_model, args.embedding_model, args.timeout_seconds)

    output = args.output or args.input.with_name(f"{args.input.stem}_eval.json")
    report = {
        "script_version": "model_prediction_eval_v1",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "input_file": str(args.input),
        "sampled_predictions": len(rows),
        "ragas_models": {
            "llm_model": args.llm_model,
            "embedding_model": args.embedding_model,
        },
        "summary": summarize(results),
        "per_prediction": results,
    }
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(json.dumps(report, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"[EVAL] Reporte guardado en: {output}", flush=True)


def main() -> None:
    args = parse_args()
    asyncio.run(main_async(args))


if __name__ == "__main__":
    main()
